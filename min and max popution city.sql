with cte as (select *, row_number() over(partition by state order by population) as min_population,
          row_number() over(partition by state order by population desc) as max_population
from city_population)
select state, min(case when min_population=1 then city end) as min_pop_city,
              max(case when max_population=1 then city end) as max_pop_city
              from cte group by state;